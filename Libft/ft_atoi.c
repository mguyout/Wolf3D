#include "libft.h"

int		ft_atoi(const char *str)
{
	int a;
	int b;
	int i;

	a = 0;
	b = 0;
	i = 0;
	while (str[i] == '\v' || str[i] == '\n' || str[i] == '\r'
	|| str[i] == '\f' || str[i] == '\t' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		a = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		b *= 10;
		b += ((int)str[i] - 48);
		i++;
	}
	if (a)
		return (-b);
	return (b);
}
