#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int x;

	x = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[x])
	{
		s1[i] = s2[x];
		i++;
		x++;
	}
	s1[i] = '\0';
	return (s1);
}
